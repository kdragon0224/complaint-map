/**
 * 민자 고속도로 여부 확인 (ETC 4자리 코드 기준)
 */
export function getPrivateOperator(etcCode: string): string | null {
  return PRIVATE_ROAD_CODES[etcCode] ?? null;
}

/**
 * ETC 4자리 코드와 이정(km)으로 관할 지역본부/지사 조회
 */
export function lookupJurisdiction(etcCode: string, km: number): { hq: string; branch: string } | null {
  if (getPrivateOperator(etcCode)) return null;

  for (const rule of JURISDICTION_RULES) {
    if (rule.etcCode === etcCode && km >= rule.kmStart && km <= rule.kmEnd) {
      return { hq: rule.hq, branch: rule.branch };
    }
  }
  return null;
}

/**
 * 관할 정보로 표시용 문자열 생성
 */
export function formatAgency(etcCode: string, km: number, fallbackIcName?: string): string {
  const privateOp = getPrivateOperator(etcCode);
  if (privateOp) return privateOp;

  const j = lookupJurisdiction(etcCode, km);
  if (j) return `한국도로공사 ${j.hq} ${j.branch}`;
  if (fallbackIcName) return `한국도로공사 (${fallbackIcName} 인근)`;
  return '한국도로공사';
}
